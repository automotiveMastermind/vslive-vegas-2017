namespace AM.VSLive.Library
{
    /// <summary>
    /// Represents an example.
    /// </summary>
    public class Example
    {
        /// <summary>
        /// Divides the left operand by the right operand.
        /// </summary>
        /// <param name="left">
        /// The left operand of the divide operation.
        /// </param>
        /// <param name="right">
        /// The right operand of the divide operation.
        /// </param>
        /// <returns>
        /// The result of the operation.
        /// </returns>
        public float Divide(int left, int right)
        {
            return left / right;
        }
    }
}
